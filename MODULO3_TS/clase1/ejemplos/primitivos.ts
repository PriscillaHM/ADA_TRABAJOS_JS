//Tipos de primitivos en typescript

//1. String 
let nombre: string = "ADA Lovelace"
let saludo: string = `Hola ${nombre} bienvenida a TS`
console.log(saludo)

//2. Number
let entero: number = 42
let decimal: number = 3.14
let cientifico: number = 1e5
console.log('Entero: ', entero)
console.log('Decimal: ', decimal)
console.log('Cientifico: ', cientifico)

//3. Boolen
let esEstudiante: boolean = true
let tieneLicencia: boolean = false
console.log('Es estudiante? ', esEstudiante)
console.log('Tiene licencia? ', tieneLicencia)

//4. Null
let valorNull: null = null
console.log('Valor nulo: ', valorNull)

//5. Undefined
let valorIndefinido: undefined = undefined
console.log('Valor indefinido: ', valorIndefinido)