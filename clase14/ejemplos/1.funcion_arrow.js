//EJEMPLO 1 Funcion clasica declarada
function multiplicarPorDos(num){
    return num * 2;
}

console.log(multiplicarPorDos(5));

//EJEMPLO 2 Funcion arrow expresada
let multiplicarPorDorArrow = (num) => {
    return num * 2;
}

console.log(multiplicarPorDorArrow(8));

//EJEMPLO 3
const multiplicarPorDorArrowSimple = num => num * 2;

console.log(multiplicarPorDorArrowSimple(9));